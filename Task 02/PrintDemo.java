class PrintDemo{
	public static void main(String args[]){

		System.out.println("My Name is Thivviya");
		System.out.println("My Father Name is Krishnakumar");
		System.out.println("My age is 21");
		
		System.out.println();
		
		System.out.print("My Name is Thivviya");
		System.out.print("My Father Name is Krishnakumar");
		System.out.print("My Age is 21");
		
		System.out.println();
		
		System.out.print("My Name is Thivviya\n");
		System.out.print("My Father Name Krishnakumar\n");
		System.out.print("My age is 21\n");
		
		System.out.println();
		System.out.print("My Name is Thivviya\t");
		System.out.print("My Father Name Krishnakumar\t");
		System.out.print("My age is 21\t");
		
	
		
	}
}